# 微实例8.4.1数据组织的维度
"""
一维数据由对等关系的有序或无序数据构成，采用线性方式组织，对应于数学中的数组和集合等概念。
"""
# 用列表存储一组温度数据
temp_data = [23.5, 24.8, 22.1, 25.3, 21.7]

# 计算平均值
avg_temp = sum(temp_data) / len(temp_data)
print(f"平均温度：{avg_temp:.1f}℃")  # 输出：平均温度：23.5℃

# 用逗号分隔存储数据
csv_str = ",".join(map(str, temp_data))
print("存储格式：", csv_str)  # 输出：23.5,24.8,22.1,25.3,21.7

"""
二维数据，也称表格数据，由关联关系数据构成，采用表格方式组织，
对应于数学中的矩阵，常见的表格都属于二维数据。
"""
# 学生成绩表（行：学生，列：科目）
score_table = [
    ["姓名", "数学", "语文", "英语"],
    ["张三", 85, 92, 78],
    ["李四", 76, 88, 95],
    ["王五", 90, 85, 92]
]

# 遍历二维数据
print("\n成绩单：")
for row in score_table:
    # 用制表符对齐输出
    print("\t".join(map(str, row)))

# 计算每个学生平均分
print("\n平均分：")
for student in score_table[1:]:  # 跳过表头
    avg = sum(student[1:]) / (len(student) - 1)
    print(f"{student[0]}：{avg:.1f}")
